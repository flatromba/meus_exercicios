package Exercicio04medio;

public class Exercicio04medio {
	
	public void retornaMultiplicacao() {
		int multiplicacao=1;
		for (int i=1;i<=1000;i++) {
			multiplicacao=multiplicacao*i;
			if (multiplicacao>=1000) {
				System.out.println(multiplicacao=1);
			}
			else {
				System.out.println(multiplicacao);
			}
		}
	}
}