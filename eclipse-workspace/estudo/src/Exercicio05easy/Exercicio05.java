package Exercicio05easy;

public class Exercicio05 {
	
	public int retornaSoma(int numero1, int numero2, int numero3) {
		int soma=numero1+numero2+numero3;
		return soma;
	}
	
	public int retornaSubtracao(int numero1, int numero2, int numero3) {
		int subtracao=numero1-numero2-numero3;
		return subtracao;
	}
	
	public int retornaMultiplicacao(int numero1, int numero2, int numero3) {
		int multiplicacao=numero1*numero2*numero3;
		return multiplicacao;
	}
	
	public int retornaMedia(int numero1, int numero2, int numero3) {
		int media=(numero1+numero2+numero3)/3;
		return media;
	}
}