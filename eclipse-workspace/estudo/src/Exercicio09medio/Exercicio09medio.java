package Exercicio09medio;

public class Exercicio09medio {
	
	public int[] retornaNumeroAluno(int vetorNumero[]) {
		int aluno=10;
		for (int i=0;i<aluno;i++) {
			System.out.println(vetorNumero[i]);
		}
		return vetorNumero;
		
	}
	
	public String[] retornaNomeAluno(String vetorNome[]) {
		int aluno=10;
		for (int i=0;i<aluno;i++) {
			System.out.println(vetorNome[i]);
		}
		return vetorNome;
	}
}